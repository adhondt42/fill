#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int p;

	p = 0;
	i = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[p] != '\0')
		s1[i++] = s2[p++];
	s1[i] = '\0';
	return (s1);
}
